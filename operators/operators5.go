package main

import (
	"fmt"
)

func main() {
	var x = 5
	var y = 3
	fmt.Println(x > y) // returns 1 (true) because 5 is greater than 3
}

// ==	Equal to	x == y
// !=	Not equal	x != y
// >	Greater than	x > y
// <	Less than	x < y
// >=	Greater than or equal to	x >= y
// <=	Less than or equal to	x <= y
